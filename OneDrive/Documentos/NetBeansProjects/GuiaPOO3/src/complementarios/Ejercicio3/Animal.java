public class Animal {

    String nombre;
    int edad;
    String alimento;

    //Getter y Setter
    String getNombre() {
        return nombre;
    }

    void setNombre(String nombre) {
        this.nombre = nombre;
    }

    int getEdad() {
        return edad;
    }

    void setEdad(int edad) {
        this.edad = edad;
    }

    String getAlimento() {
        return alimento;
    }

    void setAlimento(String alimento) {
        this.alimento = alimento;
    }

}
